	public function getDeptartmentProducts($depart_id){

	    $query = $this->db->query(" SELECT pd.`product_id`,pd.`name`,pd.`description`,pd.`short_description` FROM " . DB_PREFIX . "product_description pd 
                INNER JOIN " . DB_PREFIX . "product_to_category pc ON pd.`product_id` = pc.`product_id`
