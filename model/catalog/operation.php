                WHERE c.`depart_id`='".$depart_id."' AND pd.`product_id` > 882 ");

	    return $query->rows;

    }


    public function getAllCategories(){
        $query = $this->db->query(" SELECT * FROM oc_category c
            INNER JOIN oc_category_description cd ON c.`category_id` = cd.`category_id`");

        return $query->rows;
    }


    public function setProductName($id,$name){
